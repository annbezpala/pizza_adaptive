$('#show').click(function(){
    $('#show').css('display', 'none');
    $('#hide').css('display', 'block');
    $('.menu-list').css('display', 'block');
});

$('#hide').click(function(){
    $('#hide').css('display', 'none');
    $('#show').css('display', 'block');
    $('.menu-list').css('display', 'none');
});


